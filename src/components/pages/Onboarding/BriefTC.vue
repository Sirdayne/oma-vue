<script>
import FooterBlock from '@/components/partials/Footer'
import { errorHandler } from '@/plugins/CustomAPI'

export default {
  components: { FooterBlock },
  methods: {
    acceptTAndC () {
      let userData = this.$store.getters.getUserData
      let onboardingData = this.$store.getters.getOnboardingData
      this.$API.updateTandC({
        cuid: userData.id,
        data: {
          cuid: userData.id,
          orgId: onboardingData.organization.id,
          acceptTandC: 'Y'
        }
      }).then(res => {
        let onboardingData = this.$store.getters.getOnboardingData
        let isAdmin = this.$store.getters.isAdmin
        onboardingData.acceptTandC = 'Y'
        this.$store.dispatch('setOnboardingData', onboardingData)
        if (onboardingData.organization.mcRegStatus === 'Completed') {
          this.$router.push('/dashboard')
        } else if (onboardingData.organization.mcRegStatus === 'Pending') {
          this.$router.push('/organization-under-review')
        } else if (!onboardingData.organization.mcRegStatus) {
          if (isAdmin) {
            this.$router.push('/org-reg')
          } else {
            this.$router.push('/contact-org-admin')
          }
        } else {
          this.$router.push('/contact-org-admin')
        }
      }).fail(err => {
        this.$store.dispatch('setGenericError', {
          title: 'Something went wrong',
          text: 'Sorry, were unable to connect to our systems at this time. Please try again later.',
          btn: 'Retry'
        })
        errorHandler(err)
      })
    }
  }
}
</script>
